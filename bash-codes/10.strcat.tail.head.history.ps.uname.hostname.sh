echo $STR1

echo ""
echo "#########################################################################"
echo "#################### HISTORY                     ########################"
echo "#########################################################################"
echo ""

history | tail -5


echo ""
echo "#########################################################################"
echo "#################### PS -EF  vs pgrep -lfU       ########################"
